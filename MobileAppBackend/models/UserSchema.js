const mongoose = require("mongoose");
const UserSchema = new mongoose.Schema({
  fName: {
    type: String,
    required: true,
  },
  lName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  userState: {
    type: Boolean,
  },
});
module.exports = mongoose.model("User", UserSchema);
